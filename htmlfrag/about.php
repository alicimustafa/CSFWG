<h3>Our Mission</h3>
<p>At Colorado Springs Fiction Writers Group our goal is simple. To make writers write better. 
Whatever your writing goals are, we want to make them happen for you. We offer all sorts of ways to help you. 
We have critique groups, workshops, anthologies, brainstorming sessions, and even write-ins.
</p>
<p>We are a 501(c)(3) nonprofit. 
</p>
<h3>Critique Groups</h3>
<P>The four critique groups are the backbone of CSFWG. The groups are designed to give an author honest feedback on a story or poem, 
providing both praise and suggestions for improvement. Visitors are always welcome, but only members may submit works for critique.
</P>
<h3>They WorkHow </h3>
<p>You can attend up to three meetings, critique, and participate in anything that comes up, before you are offered a 
membership and opportunity to submit your own work.
</p>
<P>Everyone can submit up to 50 pages of double spaced work. The other members critique that work and bring it back the following the month. 
This gives them opportunity to give it their time and attention.
</P>
<p>Then, everyone is given a turn to go over the main point of the critique verbally.
</p>
<h3>Workshops
</h3>
<p>CSFWG offers workshops on a wide variety of topics. Anything from polishing up your grammar, to plotting your great novel, to tips from a real coroner. 
There can almost anything that might spark your imagination. All workshops are free and open to the public. We love guests. Often there are donuts. They are bimonthly.
</p>
<h3>Write-Ins</h3>
<p>It is often hard to find a time and place to write where family, friends, and other pressing matters don’t interrupt. 
Sometimes, having a little peer pressure can help. Take on that next writing challenge with the support of your fellow writers. 
Check out our calendar for times and locations. Everyone is welcome at our write-ins. 
</p>
<h3>Brainstorming Sessions
</h3>
<p>Sometimes you have an idea, but you aren’t quite sure how to get it off the ground. That’s why we started our 
Brainstorming Sessions. Join your fellow writers as we gather to help each other out of our plot holes, wacked characters, and other literary messes. 
</p>
<p>Our brainstorming sessions are first Thursday of every month and open to the public. Check the calendar for times and locations.
</p>
<h3>Anthologies</h3>
<p>Our groups is proud of have in house produced two Anthologies -An Uncommon Collection and Remnants & Resolutions: Tales of Survival. 
We are making it a bi-yearly event. Every Anthology has a theme. Our upcoming one is all about Colorado. It is scheduled for release in late 2016. 
</p>
